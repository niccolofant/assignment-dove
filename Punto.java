
public class Punto {

  private double x;
  private double y;

  /**
   * Costruttore per la creazione della rappresentazione di un punto
   * @param x Coordinata x del punto
   * @param y Coordinata y del punto
   */
  public Punto(double x, double y) {
    this.x = x;
    this.y = y;
  }

  public double getX() {
      return x;
  }

  public double getY() {
      return y;
  }

}
